        // TODO add your handling code here:
        Pasajero pasajero = new Pasajero(jtxtnombres.getText(),
        jtxtapellidos.getText(),jtxtpasaporte.getText(),
        jtxtnacionalidad.getText());
        
        int index = seleccion.getSelecion();
        int indexC = seleccioncompa침ia.getSelecion();
        int indexV = seleccionvuelo.getSelecion();
        String registros[] = metodosdb.getVueloInfo(indexC,indexV);
        
        if (Integer.parseInt(registros[5])<Integer.parseInt(registros[4])){
            int num =Integer.parseInt(registros[5]);
            num++;
            metodosdb.InsertarPasajeros(pasajero, indexV,num);     
            ListaPasajerosForm form = new ListaPasajerosForm(indexAA);
            form.setVisible(true);
            this.dispose();
           
            
        }else{
            JOptionPane.showMessageDialog(null,"Se super칩 el limite m치ximo de pasajeros");
        
        }
            
        
        
        jtxtnombres.setText("");
        jtxtapellidos.setText("");
        jtxtpasaporte.setText("");
        jtxtnacionalidad.setText("");
        
